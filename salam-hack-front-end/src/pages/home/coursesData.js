function getCoursesByKeywords(keywords) {
  if (typeof keywords === "string") {
    keywords = [keywords];
  }

  const categoryMap = {
    برمجة: "programming",
    تصميم: "design",
    تسويق: "markting",
  };

  const filteredCourses = [];

  keywords.forEach((keyword) => {
    if (categoryMap[keyword]) {
      const category = categoryMap[keyword];
      filteredCourses.push(coursesData[category]);
    }
  });

  return filteredCourses;
}

